/**
 * @author laohu
 * @param {String} fruit 请给我一个水果 
 */
function show(fruit) {
  if (fruit === "苹果") {
    console.log("天地一号")
  } else if (fruit === "梨子") {
    console.log("冰糖雪梨茶")
  } else if (fruit === "香蕉") {
    console.log("香蕉牛奶")
  } else {
    console.log("您不能丢入这个东西")
  }
}

/**
 function 声明一个函数
 show 函数的名字
 () 形参存放的地点 void 0-正无穷个
 {} 函数体 每当调用函数的时候会执行大括号中的内容
 函数不关注过程 只关注入口和出口
 函数的封装性
 */